#!/usr/bin/env bash
# Build raylib for WebAssembly using emscripten
# Produces: neo_mods/raylib/raylib.js (self-contained, WASM embedded)
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
RAYLIB_VERSION="5.0"
RAYLIB_DIR="$SCRIPT_DIR/raylib-$RAYLIB_VERSION"
BUILD_DIR="$SCRIPT_DIR/output"
FINAL_JS="$SCRIPT_DIR/../raylib.js"

# ── Download raylib source ────────────────────────────────────────
if [ ! -d "$RAYLIB_DIR" ]; then
  echo "→ Downloading raylib $RAYLIB_VERSION source..."
  cd "$SCRIPT_DIR"
  curl -sL "https://github.com/raysan5/raylib/archive/refs/tags/$RAYLIB_VERSION.tar.gz" \
    | tar xz
  echo "  Done."
fi

# ── Build libraylib.a for PLATFORM_WEB ────────────────────────────
RAYLIB_A="$BUILD_DIR/libraylib.a"
if [ ! -f "$RAYLIB_A" ]; then
  echo "→ Compiling raylib for PLATFORM_WEB..."
  mkdir -p "$BUILD_DIR"

  RAYLIB_SRC="$RAYLIB_DIR/src"
  CFLAGS="-Os -DPLATFORM_WEB -DGRAPHICS_API_OPENGL_ES2"
  SOURCES=(
    "$RAYLIB_SRC/rcore.c"
    "$RAYLIB_SRC/rshapes.c"
    "$RAYLIB_SRC/rtextures.c"
    "$RAYLIB_SRC/rtext.c"
    "$RAYLIB_SRC/rmodels.c"
    "$RAYLIB_SRC/utils.c"
    "$RAYLIB_SRC/raudio.c"
  )

  OBJECTS=()
  for src in "${SOURCES[@]}"; do
    obj="$BUILD_DIR/$(basename "${src%.c}.o")"
    echo "  Compiling $(basename "$src")..."
    emcc $CFLAGS -I"$RAYLIB_SRC" -I"$RAYLIB_SRC/external/glfw/include" -c "$src" -o "$obj"
    OBJECTS+=("$obj")
  done

  echo "  Creating libraylib.a..."
  emar rcs "$RAYLIB_A" "${OBJECTS[@]}"
  echo "  Done."
fi

# ── Compile C bridge + raylib → single JS+WASM bundle ────────────
BRIDGE_C="$SCRIPT_DIR/raylib_bridge.c"
EM_OUTPUT="$BUILD_DIR/raylib_em.js"

echo "→ Compiling bridge + raylib to WASM..."
emcc "$BRIDGE_C" "$RAYLIB_A" \
  -I"$RAYLIB_DIR/src" \
  -o "$EM_OUTPUT" \
  -Os \
  -sMODULARIZE=1 \
  -sEXPORT_NAME=createRaylib \
  -sSINGLE_FILE=1 \
  -sUSE_GLFW=3 \
  -sALLOW_MEMORY_GROWTH=1 \
  -sASYNCIFY \
  -sASYNCIFY_STACK_SIZE=65536 \
  -sEXPORTED_RUNTIME_METHODS='["cwrap","ccall","UTF8ToString","stringToUTF8","stringToNewUTF8"]' \
  -sEXPORTED_FUNCTIONS="$(cat "$SCRIPT_DIR/exported_functions.txt")" \
  -sENVIRONMENT=web,node \
  --no-entry

echo "  Done."

# ── Assemble final raylib.js ──────────────────────────────────────
echo "→ Assembling final raylib.js..."
WRAPPER_JS="$SCRIPT_DIR/raylib_wrapper.js"

cat > "$FINAL_JS" <<'HEADER'
'use strict';
// ═══════════════════════════════════════════════════════════════════
// NeoBasic Raylib Module — WASM-backed implementation
// Auto-generated by build.sh — do not edit manually
// ═══════════════════════════════════════════════════════════════════

HEADER

# Inline the emscripten module factory
echo "// ── Emscripten runtime (createRaylib factory) ──" >> "$FINAL_JS"
cat "$EM_OUTPUT" >> "$FINAL_JS"
echo "" >> "$FINAL_JS"

# Inline the wrapper code
echo "// ── NeoBasic wrappers ──" >> "$FINAL_JS"
cat "$WRAPPER_JS" >> "$FINAL_JS"

SIZE=$(wc -c < "$FINAL_JS" | tr -d ' ')
echo "  Final raylib.js: $SIZE bytes"
echo "→ Build complete!"
